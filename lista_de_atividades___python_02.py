"""10) Escreva um algoritmo que leia um valor inicial A e imprima a seqüência de valores do cálculo de
A! e o seu resultado. Ex: 5! = 5 X 4 X 3 X 2 X 1 = 120
"""

import math

A = int(input("Digite um valor inicial A: "))

if A < 0:
    print("O valor deve ser um número inteiro não negativo.")
else:
    resultado = math.factorial(A)
    fatores = " x ".join(str(i) for i in range(A, 0, -1))
    print(f"{A}! = {fatores} = {resultado}")