"""**2**) Desenvolver um algoritmo que leia a altura de 15 pessoas. Este programa deverá calcular e
mostrar :
a. A menor altura do grupo;
b. A maior altura do grupo;
"""

menor_altura = float('inf')
maior_altura = float('-inf')

